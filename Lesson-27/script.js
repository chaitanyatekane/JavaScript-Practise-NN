// switch statements

const currValue = 'D';
switch(currValue){
    case 'A':
        console.log("That's A");
        break;
    case 'B': 
        console.log("That's B");   
        break;
    case 'C':
        console.log("That's C");
        break;
    case 'D':
        console.log("That's D");
        break;
    case 'E':
        console.log("That's E");
        break;
    case 'F':
        console.log("That's F");
        break;
    default:
        console.log("No Input Character");
        break;
}
// That's D

